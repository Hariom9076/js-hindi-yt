const month = "jan"
switch (month) {
    case "jan":
        console.log("it is january");
        break;
    case "feb":
        console.log("it is february");
        break;
    case "march":
        console.log("it is march");
        break;
    case "april":
        console.log("it is april");
        break;

    default:
        console.log("it is default");
        break;
}

// agar break nhi lagaoge toh neeche ka saara code execute ho jayega except default case